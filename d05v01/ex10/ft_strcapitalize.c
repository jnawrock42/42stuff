char	*ft_strlowcase(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 'A' && str[i] <= 'Z')
			str[i] = str[i] + 32;
		i++;
	}
	i = 0;
	while (str[i] != '\0')
		if (str[i] >= 'a' && str[i] <= 'z' && str[i - 1] == ' ') 
			str[i] = str[i] - 32;
		i++;
	return (str);
}
